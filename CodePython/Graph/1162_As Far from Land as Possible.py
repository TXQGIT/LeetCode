class Solution:
    def maxDistance(self, grid):
        # 思路：首先将所有陆地放入队列中，然后进行BFS
        # 由于BFS的性质，可以保证每个0首次被访问时，一定是离其最近的1来访问的
        n = len(grid)
        if n == 0:
            return -1
        dis = [[0] * n for _ in range(n)]
        queue = []
        for r in range(n):
            for c in range(n):
                if grid[r][c] == 1:
                    queue.append((r, c))
        while len(queue):
            (r, c) = queue.pop(0)
            if r > 0 and grid[r - 1][c] == 0 and dis[r - 1][c] == 0:
                dis[r - 1][c] = dis[r][c] + 1
                queue.append((r - 1, c))
            if c > 0 and grid[r][c - 1] == 0 and dis[r][c - 1] == 0:
                dis[r][c - 1] = dis[r][c] + 1
                queue.append((r, c - 1))
            if r < n - 1 and grid[r + 1][c] == 0 and dis[r + 1][c] == 0:
                dis[r + 1][c] = dis[r][c] + 1
                queue.append((r + 1, c))
            if c < n - 1 and grid[r][c + 1] == 0 and dis[r][c + 1] == 0:
                dis[r][c + 1] = dis[r][c] + 1
                queue.append((r, c + 1))
        ans = max(max(dis[:]))
        return ans if ans > 0 else -1


